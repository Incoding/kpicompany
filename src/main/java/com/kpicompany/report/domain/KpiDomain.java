package com.kpicompany.report.domain;




public class KpiDomain {
    private Double id;
    private String kpi;
    private String name;

    public Double getId() {
        return id;
    }

 
    public void setId(Double id) {
        this.id = id;
    }

    public String getKpi() {
        return kpi;
    }

    public void setKpi(String kpi) {
        this.kpi = kpi;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
